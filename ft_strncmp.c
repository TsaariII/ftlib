#include "libft.h"

int ft_strncmp(const char *s1, const char *s2, size_t num)
{
    while (*s1 != '\0' && *s2 != '\0' && *s1 == *s2 && num - 1)
    {
        s1++;
        s2++;
    }
    return (*s1 - *s2);
}